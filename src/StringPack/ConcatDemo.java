package StringPack;

public class ConcatDemo {
	
	
	public static void main(String []args) {
		
		
		
		String firstName="ashish";
		String lastName="Bahurupi";
		String space=" ";
		
		
		String fullName=firstName.concat(space).concat(lastName);
		
		
		System.out.println(fullName);
		
		
		
	}

}
